package algorithms.compGeometry;

/**
 *
 * @author nichole
 */
public class XY {

    protected final float[] x;
    protected final float[] y;

    public XY(float[] xArray, float[] yArray) {
        this.x = xArray;
        this.y = yArray;
    }

    public float[] getX() {
        return x;
    }

    public float[] getY() {
        return y;
    }
}
