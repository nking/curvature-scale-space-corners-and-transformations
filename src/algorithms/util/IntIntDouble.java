package algorithms.util;

public class IntIntDouble {
    private final int a;
    private final int b;
    private final double c;
    public IntIntDouble(int a, int b, double c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }
    public int getA() {
        return a;
    }
    public int getB() {
        return b;
    }
    public double getC() {
        return c;
    }
}
